    static String ByteToHex(int i)
    {
        String s = new String(Integer.toHexString(i));
        if(s.length() > 2)
            s = s.substring(s.length() - 2, s.length());
        return s;
    }

    static String ShortToHex(int i)
    {
        String s = new String(Integer.toHexString(i));
        if(s.length() > 4)
            s = s.substring(s.length() - 4, s.length());
        return s;
    }
